#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	int		len;
	size_t	i;

	len = ft_strlen(s1);
	str = malloc(sizeof(char) * len + 1);
	if (!str)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = 0;
	return (str);
}
